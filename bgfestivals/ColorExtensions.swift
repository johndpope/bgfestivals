import UIKit

extension UIColor {
    
    static func mainApp() -> UIColor {
        return UIColor(red: 2/255, green: 183/255, blue: 156/255, alpha: 1)
    }
    
    static func darkGreen() -> UIColor {
        return UIColor(red: 0/255, green: 119/255, blue: 47/255, alpha: 1)
    }
    
}
